"""AgentMarkdownWriter — 生成 CLI 可加载的 agent / skill markdown 文件。

从 AgentDefinition + SkillSpec 生成:
  - ``.claude/agents/<name>.md`` — CLI ``@agent-name`` 可加载
  - ``.atomcode/skills/<name>/SKILL.md`` — skill 定义 + 操作参考
  - ``.atomcode/skills/<name>/reference/...`` — 嵌入源码片段
  - ``clawcodex-overview.md`` — 总览 Agent（知晓所有子 Agent 职责和调用链）

设计决定:
  - 使用 Jinja2 模板渲染，模板可自定义（传入 templates_dir）
  - 总览 Agent 始终生成（无额外参数）
  - `.claude/agents/*.md` 是本路径的默认输出格式
  - 技能参考脚本用文件嵌入而非模板渲染
"""

from __future__ import annotations

import logging
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any

from jinja2 import Template

logger = logging.getLogger(__name__)

# ---------------------------------------------------------------------------
# Data containers for overview agent
# ---------------------------------------------------------------------------


@dataclass
class AgentComponentInfo:
    """子 Agent 的信息，供总览 Agent 决策。"""
    name: str                         # "video-ops-agent"
    description: str                  # "视频算子处理：转码、切片、水印"
    capabilities: list[str] = field(default_factory=list)
    input_types: list[str] = field(default_factory=list)
    output_types: list[str] = field(default_factory=list)
    invoke_pattern: str = ""          # '@video-ops-agent 将 input.mp4 转码为 HLS'


@dataclass
class WorkflowStage:
    """工作流的一个阶段，描述阶段之间的衔接关系。"""
    name: str                         # "视频预处理"
    order: int                        # 阶段序号
    description: str = ""             # "对原始视频进行格式检测和标准化"
    responsible_agent: str = ""       # "video-ops-agent"
    depends_on: list[str] | None = None  # 前置阶段
    output_type: str = ""             # "标准化视频"


# ---------------------------------------------------------------------------
# Default Jinja2 templates
# ---------------------------------------------------------------------------

_AGENT_MD_TEMPLATE_SRC = """\
---
name: {{ name }}
description: {{ description }}
{% if model %}model: {{ model }}{% endif %}
tools:
{% for tool in tools %}
  - {{ tool }}
{% endfor %}
{% if skills %}skills:
{% for skill in skills %}
  - {{ skill }}
{% endfor %}{% endif %}
{% if when_to_use %}when_to_use: {{ when_to_use }}{% endif %}
---

# Agent: {{ name }}

{{ description }}

## 使用说明

This agent is auto-generated by POS Converter. Use `@{{ name }}` to invoke it.
"""

_SKILL_MD_TEMPLATE_SRC = """\
---
name: {{ name }}
description: {{ description }}
user-invocable: true
{% if allowed_tools %}allowed-tools:
{% for tool in allowed_tools %}
  - {{ tool }}
{% endfor %}{% endif %}
---

# Skill: {{ name }}

{{ description }}

## 参数说明

{% if parameters %}
| 参数名 | 类型 | 必填 | 说明 |
|--------|------|------|------|
{% for param in parameters %}| `{{ param.name }}` | `{{ param.type_hint or '—' }}` | {{ '是' if param.required else '否' }} | {{ param.description }} |
{% endfor %}
{% else %}
无参数。
{% endif %}

## 源码参考

{% if source_code %}
```python
{{ source_code }}
```
{% endif %}
"""

_OVERVIEW_AGENT_TEMPLATE_SRC = """\
---
name: {{ name }}
description: {{ description }}
model: {{ model or 'default' }}
tools:
  - "*"
skills:
{% for skill in all_skills %}
  - {{ skill }}
{% endfor %}
when_to_use: {{ description }}
---

# {{ name }} - 总览 Agent

{{ description }}

## 工作流概述

{% for stage in workflow_stages %}
### 阶段 {{ stage.order }}: {{ stage.name }}

{{ stage.description }}

- **负责 Agent**: `@{{ stage.responsible_agent }}`
{% if stage.depends_on %}- **前置阶段**: {% for dep in stage.depends_on %}`{{ dep }}`{% if not loop.last %}, {% endif %}{% endfor %}{% endif %}
- **输出**: {{ stage.output_type }}

{% endfor %}
## 子 Agent 清单

{% for agent in component_agents %}
### @{{ agent.name }}

{{ agent.description }}

**能力**: {% for cap in agent.capabilities %}{{ cap }}{% if not loop.last %}, {% endif %}{% endfor %}

**输入类型**: {% for t in agent.input_types %}{{ t }}{% if not loop.last %}, {% endif %}{% endfor %}

**输出类型**: {% for t in agent.output_types %}{{ t }}{% if not loop.last %}, {% endif %}{% endfor %}

**调用方式**: `{{ agent.invoke_pattern }}`

{% endfor %}
## 使用示例

{% for agent in component_agents %}
- **{{ agent.description }}**: `{{ agent.invoke_pattern }}`
{% endfor %}

## 跨组件编排

当任务需要多个子 Agent 协作时，按工作流阶段顺序依次调用。
每个阶段的输出自动作为下一阶段的输入。
"""

_WORKFLOW_TEMPLATE_SRC = """\
# WORKFLOW.md — auto-generated by POS Converter
# Edit this file to customize orchestrator behavior.

name: {{ name }}
description: {{ description }}

tracker:
  type: local

workspace:
  type: shared
  base_branch: main

hooks:
  pre_commit: ""
  pre_push: ""
  post_sync: ""

agent:
  model: {{ model or 'default' }}
  test_command: ""
  build_command: ""
  lint_command: ""

{% if workflow_stages %}
stages:
{% for stage in workflow_stages %}
  - id: "stage-{{ stage.order }}"
    name: "{{ stage.name }}"
    agent: "{{ stage.responsible_agent }}"
    depends_on: [{% for dep in stage.depends_on or [] %}"{{ dep }}"{% if not loop.last %}, {% endif %}{% endfor %}]
{% endfor %}
{% endif %}

{% if component_agents %}
agents:
{% for agent in component_agents %}
  - name: "{{ agent.name }}"
    description: "{{ agent.description }}"
    tools:
      - "*"
    skills: []
{% endfor %}
{% endif %}
"""


# ---------------------------------------------------------------------------
# AgentMarkdownWriter
# ---------------------------------------------------------------------------


class AgentMarkdownWriter:
    """从 Agent 定义 + Skill 规格生成 CLI 可加载的 markdown 文件。

    Parameters
    ----------
    templates_dir : Path | None
        可选：从文件加载 Jinja2 模板的目录。默认使用内联模板。
    """

    def __init__(self, templates_dir: Path | None = None) -> None:
        self._templates_dir = templates_dir
        self._templates: dict[str, Template] = {}

    def _get_template(self, name: str, default_src: str) -> Template:
        """获取模板（优先从文件加载，fallback 到内联）。"""
        if name not in self._templates:
            if self._templates_dir:
                tmpl_path = self._templates_dir / f"{name}.j2"
                if tmpl_path.is_file():
                    self._templates[name] = Template(
                        tmpl_path.read_text(encoding="utf-8")
                    )
                    return self._templates[name]
            self._templates[name] = Template(default_src)
        return self._templates[name]

    def write_agent(
        self,
        agent_def: dict[str, Any],
        output_dir: Path,
    ) -> Path:
        """生成 ``.claude/agents/<name>.md``。

        Parameters
        ----------
        agent_def : dict
            需要键: ``name``, ``description``, 可选的 ``model``,
            ``tools``, ``skills``, ``when_to_use``。
        output_dir : Path
            输出根目录（函数会在其中创建 ``.claude/agents/``）。

        Returns
        -------
        Path
            生成的 agent markdown 文件路径。
        """
        agents_dir = output_dir / ".claude" / "agents"
        agents_dir.mkdir(parents=True, exist_ok=True)

        name = agent_def.get("name", "unnamed-agent")
        file_path = agents_dir / f"{name}.md"

        tmpl = self._get_template("agent_md", _AGENT_MD_TEMPLATE_SRC)
        content = tmpl.render(
            name=name,
            description=agent_def.get("description", ""),
            model=agent_def.get("model"),
            tools=agent_def.get("tools", []),
            skills=agent_def.get("skills", []),
            when_to_use=agent_def.get("when_to_use"),
        )

        file_path.write_text(content.strip() + "\n", encoding="utf-8")
        logger.info("Wrote agent markdown: %s", file_path)
        return file_path

    def write_skills(
        self,
        skills: list[dict[str, Any]],
        output_dir: Path,
    ) -> list[Path]:
        """生成 ``.atomcode/skills/<name>/SKILL.md`` 和参考源码片段。

        Parameters
        ----------
        skills : list[dict]
            每个 dict 需要键: ``name``, ``description``,
            可选 ``allowed_tools``, ``parameters``, ``source_code``。
        output_dir : Path
            输出根目录。

        Returns
        -------
        list[Path]
            生成的 SKILL.md 文件路径列表。
        """
        skills_base = output_dir / ".atomcode" / "skills"
        generated: list[Path] = []

        for skill in skills:
            skill_name = skill.get("name", "unnamed-skill")
            skill_dir = skills_base / skill_name
            skill_dir.mkdir(parents=True, exist_ok=True)

            # Write SKILL.md
            skill_file = skill_dir / "SKILL.md"
            tmpl = self._get_template("skill_md", _SKILL_MD_TEMPLATE_SRC)
            content = tmpl.render(
                name=skill_name,
                description=skill.get("description", ""),
                allowed_tools=skill.get("allowed_tools", []),
                parameters=skill.get("parameters", []),
                source_code=skill.get("source_code", ""),
            )
            skill_file.write_text(content.strip() + "\n", encoding="utf-8")
            generated.append(skill_file)

            # Write reference source code snippets
            source_code = skill.get("source_code", "")
            if source_code:
                ref_dir = skill_dir / "reference"
                ref_dir.mkdir(parents=True, exist_ok=True)
                ref_file = ref_dir / f"{skill_name}.py"
                ref_file.write_text(source_code, encoding="utf-8")
                generated.append(ref_file)

        return generated

    def write_workflow(
        self,
        name: str,
        description: str,
        component_agents: list[AgentComponentInfo],
        workflow_stages: list[WorkflowStage],
        output_dir: Path,
        model: str = "default",
    ) -> Path:
        """可选生成 orchestrator WORKFLOW.md。

        Parameters
        ----------
        name : str
            Agent 名称。
        description : str
            Agent 描述。
        component_agents : list[AgentComponentInfo]
            子 Agent 清单。
        workflow_stages : list[WorkflowStage]
            工作流阶段。
        output_dir : Path
            输出根目录。
        model : str
            默认模型。

        Returns
        -------
        Path
            生成的 WORKFLOW.md 路径。
        """
        file_path = output_dir / "WORKFLOW.md"
        tmpl = self._get_template("workflow", _WORKFLOW_TEMPLATE_SRC)
        content = tmpl.render(
            name=name,
            description=description,
            model=model,
            component_agents=component_agents,
            workflow_stages=workflow_stages,
        )
        file_path.write_text(content.strip() + "\n", encoding="utf-8")
        logger.info("Wrote WORKFLOW.md: %s", file_path)
        return file_path

    def write_overview_agent(
        self,
        name: str,
        description: str,
        component_agents: list[AgentComponentInfo],
        workflow_stages: list[WorkflowStage],
        output_dir: Path,
        model: str = "default",
    ) -> Path:
        """生成总览 Agent 的 ``.claude/agents/<name>.md``。

        包含完整 frontmatter + 系统提示（工作流概述 + 子 Agent 委派指引）。

        Parameters
        ----------
        name : str
            总览 Agent 名称（推荐 ``clawcodex-overview``）。
        description : str
            总览 Agent 描述。
        component_agents : list[AgentComponentInfo]
            子 Agent 清单。
        workflow_stages : list[WorkflowStage]
            工作流阶段。
        output_dir : Path
            输出根目录。
        model : str
            默认模型。

        Returns
        -------
        Path
            生成的总览 Agent markdown 文件路径。
        """
        agents_dir = output_dir / ".claude" / "agents"
        agents_dir.mkdir(parents=True, exist_ok=True)

        file_path = agents_dir / f"{name}.md"

        # Collect all skill names from component agents
        all_skills = [f"skill-{a.name}" for a in component_agents]

        tmpl = self._get_template(
            "overview_agent", _OVERVIEW_AGENT_TEMPLATE_SRC
        )
        content = tmpl.render(
            name=name,
            description=description,
            model=model,
            all_skills=all_skills,
            component_agents=component_agents,
            workflow_stages=workflow_stages,
        )

        file_path.write_text(content.strip() + "\n", encoding="utf-8")
        logger.info("Wrote overview agent: %s", file_path)
        return file_path
