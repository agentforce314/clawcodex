"""Templates for POS to Agent conversion output.

These templates define the structure of generated Agent and Skill files.
They are used by AgentBuilder and SkillGrouper to produce the actual content.
"""

from __future__ import annotations

from dataclasses import dataclass


@dataclass
class MappingRule:
    """A mapping rule: SDK method pattern → tool name → skill name."""
    method_pattern: str
    tool_name: str
    skill_name: str
    description: str = ""


# Template for generating Agent markdown files
AGENT_TEMPLATE = """\
# Agent: {agent_name}

{description}

## Tools
{tools_list}

## Skills
{skills_list}

## Memory Scope
{memory_scope}
"""

# Template for generating Skill markdown files
SKILL_TEMPLATE = """\
# Skill: {skill_name}

{description}

## Included Tools
{tools}

## Usage

Use this skill when you need to: {description_lower}

```
/{skill_name}
```
"""

# Mapping rules template (Config layer)
MAPPING_RULES_TEMPLATE = """\
# SDK Method → Tool → Skill Mapping Rules
# Auto-generated by POS to Agent converter

## Rules
{mapping_rules}

## Usage
These rules define how SDK methods are mapped to atomic tools and then grouped into skills.
Edit this file to customize the grouping behavior.
"""

SKILL_FRAGMENT_TEMPLATE = """\
## Skill: {skill_name}

**Description:** {description}

**Tools:**
{tool_list}

**When to use:** {when_to_use}
"""